// Shared renderer used by BOTH the static site and the build script,
// so the copy-from-website output and the committed types/*.md files
// can never drift apart.

import { types, modes } from "./types.mjs";

const HEADER_NOTE =
  "Polarity: this configures how the AI should treat *you*, based on your type.\n" +
  "It is NOT a persona for the AI to adopt. Your type is the input; the AI's\n" +
  "operating style is the output.";

const ATTRIBUTION = "<!-- Generated by typecast · https://www.pablooverton.com/typecast -->";

function modeBlock(mode, directives) {
  const m = modes[mode];
  const lines = directives.map((d) => `- ${d}`).join("\n");
  return `## ${m.title}: ${m.tagline}\n\n${lines}`;
}

// One mode only: what the website copies/downloads.
export function renderConfig(typeKey, mode) {
  const t = types[typeKey];
  if (!t) throw new Error(`Unknown type: ${typeKey}`);
  if (t.stub) return `# AGENTS.md for ${typeKey} (${t.nickname})\n\nComing soon.`;
  return [
    `# AGENTS.md for ${typeKey} (${t.nickname})`,
    "",
    `> ${HEADER_NOTE.split("\n").join("\n> ")}`,
    "",
    modeBlock(mode, t[mode]),
    "",
    ATTRIBUTION,
    "",
  ].join("\n");
}

// Both blocks: what the committed types/*.md files contain (user keeps one).
export function renderBoth(typeKey) {
  const t = types[typeKey];
  if (!t) throw new Error(`Unknown type: ${typeKey}`);
  if (t.stub) {
    return `# AGENTS.md for ${typeKey} (${t.nickname})\n\n_${t.blurb}_\n\nComing soon. This type isn't filled in yet.\n`;
  }
  return [
    `# AGENTS.md for ${typeKey} (${t.nickname})`,
    "",
    `> ${HEADER_NOTE.split("\n").join("\n> ")}`,
    "> ",
    "> Keep ONE of the two mode blocks below and delete the other.",
    "",
    modeBlock("satisfaction", t.satisfaction),
    "",
    modeBlock("growth", t.growth),
    "",
    ATTRIBUTION,
    "",
  ].join("\n");
}
